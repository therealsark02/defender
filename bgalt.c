void build_bgalt(void)
{
    uint8_t rtbyte, roff, rtcnt, i;
    const uint8_t *rp;
    uint8_t *wp;
    
    rp = tdata;
    wp = alttbl;
    roff = 0xe0;
    rtcnt = 7;
    rtbyte = *rp++;
    while (wp < alttbl + sizeof(alttbl)) {
        *wp++ = roff;
        // one record per 2 pixels
        for (i = 0; i < 2; i++) {
            roff += (rtbyte & 0x80) ? -1 : 1;
            if (rtcnt == 0) {
                if (rp == tdata + sizeof(tdata)) {
                    rp = tdata;
                }
                rtbyte = *rp++;
                rtcnt = 7;
            } else {
                rtbyte <<= 1;
                --rtcnt;
            }
        }
    }
}

